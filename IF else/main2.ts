// Else IF
let per=100
if(per>=90 && per<=100){
    console.log("your grade is A+");
    }
    else if (per>=80 && per<90){
        console.log("your grade is A")
    }
    else if (per>=70 && per<80){
        console.log("your grade is B+");
        }
else if (per>=60 && per<70){
    console.log("your grade is B");
}
else{
    console.log("you have entered an invalid percentage");
}